#!/usr/bin/env python3
import argparse
import csv
import json
from collections import Counter
from datetime import datetime
from pathlib import Path
from urllib.parse import parse_qs, urlparse


SUCCESS_STATUS = "success"


def parse_timestamp(item):
    for key in ("recorded_at", "received_at"):
        value = item.get(key)
        if not value:
            continue
        try:
            return datetime.fromisoformat(value.replace("Z", "+00:00"))
        except ValueError:
            continue
    return datetime.min


def parse_page_numbers(page_url):
    if not page_url:
        return None, None
    try:
        params = parse_qs(urlparse(page_url).query)
        count = params.get("RecordCount", [None])[0]
        page_count = params.get("PageCount", [None])[0]
        count = int(count) if count and str(count).isdigit() else None
        page_count = int(page_count) if page_count and str(page_count).isdigit() else None
        return count, page_count
    except Exception:
        return None, None


def run_date_for(item, override=None):
    if override:
        return override
    for key in ("recorded_at", "received_at"):
        value = item.get(key)
        if value:
            return value[:10]
    return ""


def load_rows(results_file):
    rows = []
    for line in Path(results_file).read_text(encoding="utf-8").splitlines():
        if not line.strip():
            continue
        rows.append(json.loads(line))
    return rows


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("results_file", help="raw jsonl generated by ocr_http_service")
    parser.add_argument("daily_counts_csv", help="normalized success csv output")
    parser.add_argument("--failures-csv", help="optional failure csv output")
    parser.add_argument("--summary-json", help="optional summary json output")
    parser.add_argument("--run-date", help="override run date in YYYY-MM-DD")
    args = parser.parse_args()

    rows = load_rows(args.results_file)

    latest_success_by_task = {}
    failures = []
    status_counter = Counter()

    for item in rows:
        status = item.get("status", "")
        status_counter[status] += 1
        count_from_url, page_count_from_url = parse_page_numbers(item.get("page_url"))
        if item.get("count") is None and count_from_url is not None:
          item["count"] = count_from_url
        if item.get("page_count") is None and page_count_from_url is not None:
          item["page_count"] = page_count_from_url

        if status == SUCCESS_STATUS and item.get("count") is not None:
            key = item.get("task_id") or f"{item.get('district')}|{item.get('plate')}|{item.get('listing_age')}"
            existing = latest_success_by_task.get(key)
            if existing is None or parse_timestamp(item) >= parse_timestamp(existing):
                latest_success_by_task[key] = item
        else:
            failures.append(item)

    normalized = []
    for item in sorted(
        latest_success_by_task.values(),
        key=lambda row: (row.get("district", ""), row.get("plate", ""), row.get("listing_age", "")),
    ):
        normalized.append(
            {
                "date": run_date_for(item, args.run_date),
                "task_id": item.get("task_id", ""),
                "district": item.get("district", ""),
                "plate": item.get("plate", ""),
                "listing_age": item.get("listing_age", ""),
                "count": item.get("count", ""),
                "page_count": item.get("page_count", ""),
                "status": item.get("status", ""),
                "captcha_guess": item.get("captcha_guess", ""),
                "captcha_attempt": item.get("captcha_attempt", ""),
                "recorded_at": item.get("recorded_at", ""),
                "received_at": item.get("received_at", ""),
                "page_url": item.get("page_url", ""),
            }
        )

    counts_path = Path(args.daily_counts_csv)
    counts_path.parent.mkdir(parents=True, exist_ok=True)
    with counts_path.open("w", encoding="utf-8", newline="") as handle:
        writer = csv.DictWriter(
            handle,
            fieldnames=[
                "date",
                "task_id",
                "district",
                "plate",
                "listing_age",
                "count",
                "page_count",
                "status",
                "captcha_guess",
                "captcha_attempt",
                "recorded_at",
                "received_at",
                "page_url",
            ],
        )
        writer.writeheader()
        writer.writerows(normalized)

    if args.failures_csv:
        failures_path = Path(args.failures_csv)
        failures_path.parent.mkdir(parents=True, exist_ok=True)
        failure_rows = []
        for item in failures:
            failure_rows.append(
                {
                    "date": run_date_for(item, args.run_date),
                    "task_id": item.get("task_id", ""),
                    "district": item.get("district", ""),
                    "plate": item.get("plate", ""),
                    "listing_age": item.get("listing_age", ""),
                    "status": item.get("status", ""),
                    "count": item.get("count", ""),
                    "page_count": item.get("page_count", ""),
                    "captcha_guess": item.get("captcha_guess", ""),
                    "captcha_attempt": item.get("captcha_attempt", ""),
                    "recorded_at": item.get("recorded_at", ""),
                    "received_at": item.get("received_at", ""),
                    "alert_message": item.get("alert_message", ""),
                    "error": item.get("error", ""),
                    "invalid_value": item.get("invalid_value", ""),
                    "page_url": item.get("page_url", ""),
                }
            )

        with failures_path.open("w", encoding="utf-8", newline="") as handle:
            writer = csv.DictWriter(
                handle,
                fieldnames=[
                    "date",
                    "task_id",
                    "district",
                    "plate",
                    "listing_age",
                    "status",
                    "count",
                    "page_count",
                    "captcha_guess",
                    "captcha_attempt",
                    "recorded_at",
                    "received_at",
                    "alert_message",
                    "error",
                    "invalid_value",
                    "page_url",
                ],
            )
            writer.writeheader()
            writer.writerows(failure_rows)

    if args.summary_json:
        summary = {
            "results_file": str(Path(args.results_file)),
            "normalized_output": str(counts_path),
            "total_rows": len(rows),
            "success_rows": len(normalized),
            "failure_rows": len(failures),
            "district_count": len({row["district"] for row in normalized}),
            "plate_count": len({(row["district"], row["plate"]) for row in normalized}),
            "status_counts": dict(status_counter),
        }
        summary_path = Path(args.summary_json)
        summary_path.parent.mkdir(parents=True, exist_ok=True)
        summary_path.write_text(json.dumps(summary, ensure_ascii=False, indent=2), encoding="utf-8")

    print(
        json.dumps(
            {
                "daily_counts_csv": str(counts_path),
                "success_rows": len(normalized),
                "failure_rows": len(failures),
                "distinct_districts": len({row["district"] for row in normalized}),
                "distinct_plates": len({(row["district"], row["plate"]) for row in normalized}),
            },
            ensure_ascii=False,
        )
    )


if __name__ == "__main__":
    main()
